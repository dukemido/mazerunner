    private void characterValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_characterValueChanged
        if (character.getSelectedValue().equals("Mario")) {
            characterIcon.setIcon(new ImageIcon(CustomEngine.readFile("src/resources/mario.png")));
        } else if (character.getSelectedValue().equals("Robot")) {
            characterIcon.setIcon(new ImageIcon(CustomEngine.readFile("src/resources/robot.png")));
        }
        if (character.getSelectedValue().equals("Mushroom")) {
            characterIcon.setIcon(new ImageIcon(CustomEngine.readFile("src/resources/mushroom.png")));
        }
    }//GEN-LAST:event_characterValueChanged

