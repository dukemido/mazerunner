    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (playerName.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Please enter player name.");
            return;
        }
        this.setVisible(false);
        new MainFrame(playerName.getText(),
                PlayerCharacter.valueOf(character.getSelectedValue().toString()),
                GameDifficulty.valueOf(difficulty.getSelectedValue().toString())).setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

